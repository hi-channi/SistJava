package day0204;

public class ObMethodStart_06 {
	public static void main(String[] args) {
		
		System.out.println("자바시작");
		good(22);
		good(33);
		System.out.println("끝");
	}
	
	public static void good(int age) {
		System.out.println("안녕, 좋은아침");
		System.out.println("나의 나이는 "+age+"세 입니다.");
	}
}
